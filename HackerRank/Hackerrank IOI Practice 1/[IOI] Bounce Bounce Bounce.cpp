using namespace std;

typedef long long ll;
typedef pair<int, int> pii;

int n;

ll phi(ll n){
    ll res=n;
    for(ll i=2; i*i<=n; ++i) if(n%i==0){
        res=res*(i-1)/i;
        while(n%i==0) n/=i;
    }
    if(n>1) res=res*(n-1)/n;
    return res;
}

int main(){
    cin>>n;
    cout<<phi(n+1)<<endl;
}
